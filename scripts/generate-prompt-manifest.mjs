import fs from 'node:fs';
import path from 'node:path';

const rootDir = process.cwd();
const promptsDir = path.join(rootDir, 'lib', 'generation', 'prompts');
const snippetsDir = path.join(promptsDir, 'snippets');
const templatesDir = path.join(promptsDir, 'templates');
const outputPath = path.join(promptsDir, 'manifest.ts');

function readTrimmed(filePath) {
  return fs.readFileSync(filePath, 'utf8').trim();
}

function readSnippets() {
  const entries = fs.readdirSync(snippetsDir, { withFileTypes: true });
  const snippets = {};
  for (const entry of entries) {
    if (!entry.isFile() || !entry.name.endsWith('.md')) continue;
    const id = entry.name.replace(/\.md$/, '');
    snippets[id] = readTrimmed(path.join(snippetsDir, entry.name));
  }
  return snippets;
}

function readTemplates() {
  const entries = fs.readdirSync(templatesDir, { withFileTypes: true });
  const templates = {};
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;
    const dir = path.join(templatesDir, entry.name);
    const systemPath = path.join(dir, 'system.md');
    const userPath = path.join(dir, 'user.md');
    templates[entry.name] = {
      systemPrompt: readTrimmed(systemPath),
      userPromptTemplate: fs.existsSync(userPath) ? readTrimmed(userPath) : '',
    };
  }
  return templates;
}

function stableStringify(value) {
  return JSON.stringify(value, null, 2);
}

const snippets = readSnippets();
const templates = readTemplates();

const fileContents = `/**
 * Auto-generated by scripts/generate-prompt-manifest.mjs
 * Do not edit this file manually. Edit markdown sources under prompts/ instead.
 */

import type { LoadedPrompt, PromptId, SnippetId } from './types';

export const PROMPT_SNIPPETS = ${stableStringify(snippets)} as const satisfies Record<SnippetId, string>;

export const PROMPT_TEMPLATES = ${stableStringify(templates)} as const satisfies Record<PromptId, Omit<LoadedPrompt, 'id'>>;
`;

fs.writeFileSync(outputPath, fileContents);
console.log(`Generated ${path.relative(rootDir, outputPath)}`);
